# String é um tipo de dados que são em formato de texto

'''
Sobre o Python:

- Tipagem dinâmica e forte
- Dinamica: Sabe o tipo de informação que está sendo passada
- Forte:


Sobre o String:

- Str -> string -> texto
- Dentro de aspas tanto simples ou duplas

'''

# Aspas Simples
print('Texto com aspas Simples')

# Aspas duplas
print("Texto com aspas Duplas")

# Caracter de Escape - Ele não irá executar este caracter
print("Texto com \"Caracter de escape\"")

# R - Para mostrar o caracter de escape
print(r'Texto mostrando o \'mostrar o caracter de escape\'')

# Boas Práticas - Este método serve ao contrário também
print('texto em boas "Praticas, mostrando as aspas duplas dentro das aspas simples"')
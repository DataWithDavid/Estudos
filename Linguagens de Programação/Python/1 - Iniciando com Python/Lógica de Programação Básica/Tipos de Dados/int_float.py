# Tipos Inteiros e Flutuantes (com casas decimais)

'''
Tipo INT:

- Qualquer número sem casa decimal ou com ponto flutuante é um número inteiro
- Int sem sinal é considerado positivo, com sinal é considerado negativo

'''
# Utilizamos números literal, sendo assim literalmente imprimindo aquele valor na tela
print(12) # Tipo INT positivo
print(-10) # Tipo INT negativo

'''
Tipo Float:

- Número com ponto flutuante
- Utilizar ponto para separar casas decimais
- Considera positivo ou negativo

'''

print(1.1, 10.11) # Tipo float, mostrando que tem que ser utilizado o ponto para definir a casa decimal

print(-5.3) # Ponto flutuante negativo

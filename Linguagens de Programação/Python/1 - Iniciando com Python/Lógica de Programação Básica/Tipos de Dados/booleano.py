# Tipo de dado Bool (booleano)

'''
- Só tem dois valores 1 e 0 ou True e False
- True e False são com letras maiusculas
- Só existe duas respostas disponiveis
- Serve para tomar decisões ou questionar algo no programa. EX: Vamos jogar hoje? Resposta: True ou Falso


Tipos de operadores lógicos:

- == Usado para verificar se um valor é igual ao outro
- < Menor que
- > Maior que
- >= Maior ou Igual
- <= Menor ou Igual 
- != Diferente de

'''

print(10 == 10) # O programa vai responder True, pois ambos os valores são iguais
print(5 == 10) # O programa vai responder False, pois ambos não são iguais